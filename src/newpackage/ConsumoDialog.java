    private void iniciar() {
        try {
            ComidaDAO cDAO = new ComidaDAO();
            ServicioDAO sDAO = new ServicioDAO();
            ArrayList<ComidaDTO> comidas = cDAO.getAll();
            ArrayList<ServicioDTO> servicios = sDAO.getAll();
            
            for(ComidaDTO comida: comidas){
                comidaComboBox.addItem(comida.getCodigo_comida() + ": " + comida.getNombre());
            }
            
            for(ServicioDTO servicio: servicios){
                servicioComboBox.addItem(servicio.getCodigo_Servicio());
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ConsumoDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

