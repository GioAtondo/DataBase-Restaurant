    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        if (servicioComboBox.getSelectedItem().toString().equals("") || comidaComboBox.getSelectedItem().toString().equals("") || cantidadtext.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No todos los campos est√°n llenos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                dtoConsumo.setCodigo_servicio(servicioComboBox.getSelectedItem().toString());
                String[] datos = comidaComboBox.getSelectedItem().toString().split(": ");
                dtoConsumo.setCodigo_comida(datos[0]);
                dtoConsumo.setCantidad(cantidadtext.getText());
                JOptionPane.showMessageDialog(this, "Consumo " + dtoConsumo.getCodigo_comida()
                        + " * " + dtoConsumo.getCantidad() + " registrado.",
                         "Listo", JOptionPane.INFORMATION_MESSAGE);
                dtaConsumo.append(dtoConsumo);
                dispose();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getLocalizedMessage());
            }
        }
    }//GEN-LAST:event_agregarActionPerformed

