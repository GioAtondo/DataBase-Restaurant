    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        try {
            MesaDialog mesaD = new MesaDialog(null, true);
            MesaDTO mesa = dtaMesa.read(Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 0).toString()));
            mesaD.getEliminarBoton().setVisible(true);
            mesaD.getLocalizaciontext().setText(mesa.getLocalizacion());
            mesaD.getAgregar().setText("Modificar");
            mesaD.setDtoMesa(mesa);
            mesaD.setVisible(true);
            iniciar();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_tablaMouseClicked

