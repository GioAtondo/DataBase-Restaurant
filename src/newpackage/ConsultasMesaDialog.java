    private void iniciar() {
        try {
            tabla.getTableHeader().setReorderingAllowed(false);
            dtaMesa = new MesaDAO();
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Mesa");
            model.addColumn("Localizaci√≥n");
            tabla.setModel(model);
            ArrayList<MesaDTO> lista = dtaMesa.getAll();
            for (MesaDTO mesa : lista) {
                Object[] datos = {mesa.getCodigo_mesa(), mesa.getLocalizacion()};
                model.addRow(datos);
            }
        } catch (Exception ex) {
            Logger.getLogger(ConsultaClienteDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

