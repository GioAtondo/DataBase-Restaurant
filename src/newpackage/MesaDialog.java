    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        if (localizaciontext.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No todos los campos estan llenos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                dtoMesa.setLocalizacion(localizaciontext.getText());
                if (agregar.getText().equals("Agregar")) {
                    JOptionPane.showMessageDialog(this, "Mesa en " + dtoMesa.getLocalizacion()
                            + " registrada.",
                            "Listo", JOptionPane.INFORMATION_MESSAGE);
                    dtaMesa.append(dtoMesa);
                    dispose();
                } else {
                    dtaMesa.update(dtoMesa);
                    JOptionPane.showMessageDialog(this, "Mesa " + dtoMesa.getCodigo_mesa()
                            + " modificada.",
                            "Listo", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getLocalizedMessage());
            }
        }
    }//GEN-LAST:event_agregarActionPerformed

    private void eliminarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBotonActionPerformed
        try {
            dtaMesa.delete(dtoMesa);
            JOptionPane.showMessageDialog(this, "Mesa " + dtoMesa.getCodigo_mesa()
                            + " eliminada.", "Listo", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Este mesa tiene una Reservaci√≥n, no se puede elminar", "ADVERTENCIA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_eliminarBotonActionPerformed

