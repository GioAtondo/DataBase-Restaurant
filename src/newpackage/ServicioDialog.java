    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        if (rComboBox.getSelectedItem().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "No todos los campos estan llenos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                String[] datos = rComboBox.getSelectedItem().toString().split(":");
                dtoServicio.setCodigo_Reservacion(datos[0]);
                dtaServicio.append(dtoServicio);
                JOptionPane.showMessageDialog(this, "Servicio a la Reservacion " + dtoServicio.getCodigo_Reservacion()
                            + " registrado.",
                            "Listo", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ya se está dando servicio a esta reservación", "ADVERTENCIA", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_agregarActionPerformed

