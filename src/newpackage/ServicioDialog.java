    private void iniciar(){
        try {
            ReservacionDAO rDAO = new ReservacionDAO();
            ArrayList<ReservacionDTO> res = rDAO.getAll();
            for(ReservacionDTO reservacion: res){
                rComboBox.addItem(reservacion.getCodigo_Reservacion() + ": Mesa " + reservacion.getCodigo_Mesa() + " " + reservacion.getFecha());
            }
        } catch (Exception ex) {
            Logger.getLogger(ServicioDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

